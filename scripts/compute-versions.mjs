/**
 * Compute two per-side version hashes at build time so we can detect
 * which side of the app (patient vs admin) has actually changed between
 * deployments — without relying on Vercel's commit SHA (which always
 * changes, even for commits that don't affect either side).
 *
 * Walks the relevant source directories, hashes file paths + contents,
 * and writes constants into src/lib/_generated/build-info.ts. The two
 * /api/version/{patient,admin} endpoints read from this file at request
 * time.
 *
 * Adding/removing files inside the watched roots will naturally bump
 * the relevant hash. Pure server-side changes (like an admin-only
 * route) bump only the admin hash; the patient PWA users never see a
 * banner. And vice versa.
 */
import { readFileSync, readdirSync, statSync, mkdirSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname, relative } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createHash } from 'node:crypto';

// Use fileURLToPath so paths with spaces (e.g. "APP DEV") aren't URL-encoded.
const REPO_ROOT = fileURLToPath(new URL('..', import.meta.url));

// Patient side = the PWA the actual patient/family uses. Everything
// under src/app/ EXCEPT /kinora-admin/ and the legacy /super/, plus
// the shared lib + components used by the patient UI.
const PATIENT_INCLUDE = [
  'src/app',
  'src/lib',
  'src/components',
  'public/manifest.json',
  'public/sw.js',
];
const PATIENT_EXCLUDE = [
  'src/app/kinora-admin',
  'src/app/super',
];

// Admin side = the staff panel. Just kinora-admin/ + the shared libs
// it imports (which the patient also uses, but a change to auth-context
// should refresh both sides, which is desired).
const ADMIN_INCLUDE = [
  'src/app/kinora-admin',
  'src/lib/auth-context.tsx',
  'src/lib/supabase',
];

function hashFor(includes, excludes = []) {
  const hash = createHash('sha256');
  for (const inc of includes) {
    walk(join(REPO_ROOT, inc));
  }
  return hash.digest('hex').slice(0, 12);

  function walk(path) {
    if (!existsSync(path)) return;
    const rel = relative(REPO_ROOT, path);
    if (excludes.some((ex) => rel === ex || rel.startsWith(ex + '/'))) return;
    let stat;
    try {
      stat = statSync(path);
    } catch {
      return;
    }
    if (stat.isDirectory()) {
      const entries = readdirSync(path).sort();
      for (const e of entries) walk(join(path, e));
    } else if (stat.isFile() && /\.(tsx?|css|js|mjs|json)$/.test(path)) {
      const content = readFileSync(path);
      hash.update(rel + ':' + content.length + ':');
      hash.update(content);
    }
  }
}

const patientVersion = hashFor(PATIENT_INCLUDE, PATIENT_EXCLUDE);
const adminVersion = hashFor(ADMIN_INCLUDE);
const buildTime = new Date().toISOString();

const content = `// AUTO-GENERATED by scripts/compute-versions.mjs at build time.
// Do not edit by hand. Re-run "npm run prebuild" to refresh.

export const PATIENT_VERSION = ${JSON.stringify(patientVersion)};
export const ADMIN_VERSION = ${JSON.stringify(adminVersion)};
export const BUILD_TIME = ${JSON.stringify(buildTime)};
`;

const outPath = join(REPO_ROOT, 'src/lib/_generated/build-info.ts');
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, content, 'utf8');

console.log(
  `✓ Build versions: patient=${patientVersion} admin=${adminVersion}`
);
